# Ввод списка A из 10 элементов
A = list(map(int, input("Введите 10 элементов списка через пробел: ").split()))

# Проверка длины списка
if len(A) != 10:
    print("Список должен содержать ровно 10 элементов!")
else:
    # Находим наибольший элемент и его индекс
    max_value = max(A)
    max_index = A.index(max_value)

    # Переставляем наибольший элемент с первым
    A[0], A[max_index] = A[max_index], A[0]

    # Вывод преобразованного списка
    print("Преобразованный список:", A)
